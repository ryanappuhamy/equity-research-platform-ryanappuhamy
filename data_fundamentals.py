def _finnhub_client():
    """Lazy Finnhub client; returns None if API key not set."""
    if not config.FINNHUB_API_KEY:
        return None
    try:
        import finnhub
        return finnhub.Client(api_key=config.FINNHUB_API_KEY)
    except Exception as e:
        print(f"[error] Finnhub client init failed: {e}")
        return None


def _overall_recommendation(strong_buy: int, buy: int, hold: int, sell: int, strong_sell: int) -> str:
    """Derive overall recommendation label from analyst counts."""
    bullish = strong_buy + buy
    bearish = strong_sell + sell
    if bullish > bearish and bullish > hold:
        return "Strong Buy" if strong_buy > buy else "Buy"
    if bearish > bullish and bearish > hold:
        return "Strong Sell" if strong_sell > sell else "Sell"
    return "Hold"


def get_analyst_consensus(ticker: str) -> dict:
    """Buy/hold/sell counts and overall recommendation from Finnhub."""
    if not config.FINNHUB_API_KEY:
        return {"available": False, "note": "Set FINNHUB_API_KEY for analyst consensus"}

    try:
        client = _finnhub_client()
        if client is None:
            return {"available": False, "note": "Finnhub client unavailable"}

        trends = client.recommendation_trends(ticker.upper())
        if not trends:
            note = f"Finnhub returned no recommendation trends for {ticker}"
            print(f"[error] {note}")
            return {"available": False, "note": note}

        latest = trends[0]
        strong_buy = int(latest.get("strongBuy") or 0)
        buy = int(latest.get("buy") or 0)
        hold = int(latest.get("hold") or 0)
        sell = int(latest.get("sell") or 0)
        strong_sell = int(latest.get("strongSell") or 0)

        return {
            "available": True,
            "source": "Finnhub",
            "period": latest.get("period"),
            "strong_buy": strong_buy,
            "buy": buy,
            "hold": hold,
            "sell": sell,
            "strong_sell": strong_sell,
            "overall_recommendation": _overall_recommendation(
                strong_buy, buy, hold, sell, strong_sell
            ),
        }
    except Exception as e:
        note = f"Finnhub analyst consensus failed for {ticker}: {e}"
        print(f"[error] {note}")
        return {"available": False, "note": note}


def get_price_target(ticker: str) -> dict:
    """Mean, high, and low analyst price target from Finnhub."""
    if not config.FINNHUB_API_KEY:
        return {"available": False, "note": "Set FINNHUB_API_KEY for price targets"}

    try:
        client = _finnhub_client()
        if client is None:
            return {"available": False, "note": "Finnhub client unavailable"}

        pt = client.price_target(ticker.upper())
        if not pt or pt.get("targetMean") is None:
            note = f"Finnhub returned no price target for {ticker}"
            print(f"[error] {note}")
            return {"available": False, "note": note}

        return {
            "available": True,
            "source": "Finnhub",
            "mean": pt.get("targetMean"),
            "high": pt.get("targetHigh"),
            "low": pt.get("targetLow"),
            "median": pt.get("targetMedian"),
            "last_updated": pt.get("lastUpdated"),
        }
    except Exception as e:
        try:
            import finnhub
            if isinstance(e, finnhub.FinnhubAPIException) and getattr(e, "status_code", None) == 403:
                note = f"Finnhub price target 403 for {ticker}"
                print(f"[error] {note}")
                return {"available": False, "note": note, "finnhub_status": 403}
        except ImportError:
            pass
        note = f"Finnhub price target failed for {ticker}: {e}"
        print(f"[error] {note}")
        return {"available": False, "note": note}


def _price_target_yfinance(ticker: str) -> dict:
    """Fallback price targets from yfinance when Finnhub is unavailable."""
    try:
        pt = yf.Ticker(ticker.upper()).analyst_price_targets
        if not pt or pt.get("mean") is None:
            note = f"yfinance returned no price target for {ticker}"
            print(f"[error] {note}")
            return {"available": False, "note": note}

        return {
            "available": True,
            "source": "yfinance",
            "mean": pt.get("mean"),
            "high": pt.get("high"),
            "low": pt.get("low"),
            "median": pt.get("median"),
            "current": pt.get("current"),
        }
    except Exception as e:
        note = f"yfinance price target failed for {ticker}: {e}"
        print(f"[error] {note}")
        return {"available": False, "note": note}


def get_earnings_estimates(ticker: str) -> dict:
    """Next quarter EPS estimate from Finnhub."""
    if not config.FINNHUB_API_KEY:
        return {"available": False, "note": "Set FINNHUB_API_KEY for earnings estimates"}

    try:
        client = _finnhub_client()
        if client is None:
            return {"available": False, "note": "Finnhub client unavailable"}

        estimates = client.company_eps_estimates(ticker.upper(), freq="quarterly")
        rows = estimates.get("data") if isinstance(estimates, dict) else None
        if not rows:
            note = f"Finnhub returned no EPS estimates for {ticker}"
            print(f"[error] {note}")
            return {"available": False, "note": note}

        today = date.today().isoformat()
        future = [r for r in rows if str(r.get("period", "")) >= today]
        next_q = future[0] if future else rows[-1]

        return {
            "available": True,
            "source": "Finnhub",
            "period": next_q.get("period"),
            "quarter": next_q.get("quarter"),
            "year": next_q.get("year"),
            "eps_estimate": next_q.get("epsAvg"),
            "eps_high": next_q.get("epsHigh"),
            "eps_low": next_q.get("epsLow"),
            "num_analysts": next_q.get("numberAnalysts"),
        }
    except Exception as e:
        note = f"Finnhub earnings estimates failed for {ticker}: {e}"
        print(f"[error] {note}")
        return {"available": False, "note": note}


def get_analyst_data(ticker: str) -> dict:
    """
    Consensus analyst estimates and price targets.
    Finnhub primary (consensus, price target, EPS); FMP supplementary if configured.
    These ARE opinions — real analyst opinions, reported objectively.
    """
    out: dict = {"available": False}
    sources: list[str] = []

    if config.FINNHUB_API_KEY:
        consensus = get_analyst_consensus(ticker)
        price_target = get_price_target(ticker)
        if not price_target.get("available") and price_target.get("finnhub_status") == 403:
            print(f"[warn] Finnhub price target 403 for {ticker}, falling back to yfinance")
            price_target = _price_target_yfinance(ticker)
        earnings = get_earnings_estimates(ticker)
        out["consensus"] = consensus
        out["price_target"] = price_target
        out["earnings_estimates"] = earnings
        if any(x.get("available") for x in (consensus, price_target, earnings)):
            out["available"] = True
            sources.append("Finnhub")
        if price_target.get("available") and price_target.get("source") == "yfinance":
            if "yfinance" not in sources:
                sources.append("yfinance")

    if config.FMP_API_KEY:
        try:
            try:
                grades = _fmp_get(f"grade/{ticker}", {"limit": 10})
                out["recent_rating_actions"] = [
                    {
                        "date": g.get("date"),
                        "firm": g.get("gradingCompany"),
                        "action": g.get("newGrade"),
                        "previous": g.get("previousGrade"),
                    }
                    for g in grades[:10]
                ]
            except Exception as e:
                print(f"[error] FMP rating actions failed for {ticker}: {e}")
                out["recent_rating_actions"] = []

            try:
                pt = _fmp_get(f"price-target-consensus", {"symbol": ticker})
                out["fmp_price_target_consensus"] = pt[0] if pt else None
            except Exception as e:
                print(f"[error] FMP price target consensus failed for {ticker}: {e}")
                out["fmp_price_target_consensus"] = None

            if out.get("recent_rating_actions") or out.get("fmp_price_target_consensus"):
                out["available"] = True
                sources.append("FMP")
        except Exception as e:
            print(f"[error] FMP analyst data failed for {ticker}: {e}")

    if not out.get("available"):
        return {
            "available": False,
            "note": "Set FINNHUB_API_KEY or FMP_API_KEY for analyst consensus data",
        }

    out["source"] = ", ".join(sources)
    return out
