def get_peers(ticker: str) -> list[str]:
    """
    Peer list. FMP has a dedicated endpoint; without FMP the caller
    must supply peers manually (yfinance has no reliable peer data).
    """
    if config.FMP_API_KEY:
        try:
            data = _fmp_get(f"stock_peers", {"symbol": ticker})
            if data and isinstance(data, list):
                peers = data[0].get("peersList", [])
                return peers[: config.PEER_COUNT]
        except Exception as e:
            print(f"[error] FMP peers failed for {ticker}: {e}")
    return []


